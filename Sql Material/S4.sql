-- Aggregate Functions 

-- sum() : Generates sum value on the numeric field specified 

select sum(sal) from Emp;

-- Avg() : Generates avg value on the numeric field specified 

select avg(sal) from Emp;

-- max() : Generates max. value on the numberic field specified 

select max(sal) from Emp;

-- min() : Generates min. value on the numeric field specified 

select min(sal) from Emp;

-- Count(*) : Displays total no.of records of the current table 

select count(*) from Emp;

